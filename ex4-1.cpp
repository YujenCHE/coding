#include <stdio.h>

int main(){
    int x;
    x = 4;
    
    printf("x |= 3 ==> %d \n", x|=3);//把3給x (aka 4)
    // 3 : 0000 0011
    // 4 : 0000 0100 然後看or
    //     0000 0111 aka 7
    
    
    printf("x &= 3 ==> %d \n", x&=3);
    
    // 3 : 0000 0011
    // 4會先被放上3的值
    // 4 : 0000 0111 然後看and
    //     0000 0000
}
